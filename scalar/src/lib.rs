pub fn sum(a: u8, b: u8) -> u8 {
    let c = a+b;
    return c;
}

pub fn diff(q: i16, z: i16) -> i16 {
    let w = q-z;
    return w;
}

pub fn pro(q: i8, z: i8) -> i8 {
    let w = q*z;
    return w;
}

pub fn quo(q: f32, z: f32) -> f32 {
    let w = q/z;
    return w;
}

pub fn rem(q: f32, z: f32) -> f32 {
    let w = q%z;
    return w;
}
